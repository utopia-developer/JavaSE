package part2_Java面向对象.chapter10_封装.Quiz2_8;

public class Code {
    //创建外部类Code构造块（构造块）
    {
        System.out.println("Code的构造块");
    }
    //创建外部类Code静态代码块（静态代码块）
    static {
        System.out.println("Code的静态代码块");
    }
    //创建外部类Code构造方法（构造方法）
    public Code() {
        System.out.println("Code的构造方法");
    }
}
