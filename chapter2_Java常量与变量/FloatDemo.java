/**
 * FloatDemo
 */
public class FloatDemo {

    public static void main(String[] args) {
        // 定义单精度浮点型变量
        float f = 1234.328f;
        System.out.println("f = " + f);
        // 定义双精度浮点型变量
        double d1 = 5623.465;
        System.out.println("d1 = " + d1);
        // 将整型数据赋值给浮点型变量
        double d2 = 123;
        System.out.println("d2 = " + d2);
        // 变量间的复制
        double d3 = d2;
        System.out.println("d3 = " + d3);
    }
}